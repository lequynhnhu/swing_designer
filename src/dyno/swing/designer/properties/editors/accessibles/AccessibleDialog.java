    /** Creates new form AccessibleDialog */
    public AccessibleDialog(Frame parent, boolean modal) {
        super(parent, modal);
        initComponents();
        Item[] items = getAccessibleItems();
        if (items != null) {
            DefaultComboBoxModel model = new DefaultComboBoxModel();
            for (Item item : items) {
                model.addElement(item);
                Component component = (Component) item.getValue();
                String name = item.getName();
                if (component == null) {
                    component = new JPanel();
                }
                pAccessibles.add(component, name);
            }
            cmbTypes.setModel(model);
        }
    }

    protected Item[] getAccessibleItems() {
        return null;
    }

