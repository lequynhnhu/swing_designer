    public void validateValue() throws ValidationException {
        Item item = (Item) cmbTypes.getSelectedItem();
        Component component = (Component) item.getValue();
        if (component != null && component instanceof AccessibleEditor) {
            AccessibleEditor accessible = (AccessibleEditor) component;
            accessible.validateValue();
        }
    }

